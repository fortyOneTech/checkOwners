"""Tests for checkowners.config module."""

from __future__ import annotations

from pathlib import Path

import pytest
import yaml

from checkowners.config import load_config
from checkowners.models import Config


def _write_config(tmp_path: Path, content: str) -> Path:
    config_dir = tmp_path / ".github"
    config_dir.mkdir()
    (config_dir / "checkowners.yml").write_text(content, encoding="utf-8")
    return tmp_path


def test_load_config_defaults(tmp_path: Path) -> None:
    cfg = load_config(repo_root=tmp_path)
    assert cfg == Config()
    assert cfg.analysis.lookback_days == 180
    assert cfg.analysis.min_commits == 3
    assert cfg.analysis.top_n_owners == 2
    assert cfg.paths.exclude == ("*.lock", "dist/**", "vendor/**")
    assert cfg.output.header == "# Generated by checkOwners. Do not edit manually."
    assert cfg.output.include_unowned is False
    assert cfg.drift.mode == "commit"
    assert cfg.drift.compare_to == "auto"
    assert cfg.notifications.webhook_url == ""
    assert cfg.notifications.include_unchanged is False


def test_load_config_empty_file(tmp_path: Path) -> None:
    root = _write_config(tmp_path, "")
    cfg = load_config(repo_root=root)
    assert cfg == Config()


def test_load_config_partial_override(tmp_path: Path) -> None:
    root = _write_config(tmp_path, "analysis:\n  lookback_days: 365\n")
    cfg = load_config(repo_root=root)
    assert cfg.analysis.lookback_days == 365
    assert cfg.analysis.min_commits == 3
    assert cfg.analysis.top_n_owners == 2
    assert cfg.paths == Config().paths
    assert cfg.output == Config().output


def test_load_config_full_override(tmp_path: Path) -> None:
    content = """\
analysis:
  lookback_days: 90
  min_commits: 5
  top_n_owners: 3
paths:
  exclude:
    - "*.tmp"
    - "build/**"
output:
  header: "# Custom header"
  include_unowned: true
drift:
  mode: repo
  compare_to: main
notifications:
  webhook_url: "https://hooks.example.com/drift"
  include_unchanged: true
"""
    root = _write_config(tmp_path, content)
    cfg = load_config(repo_root=root)
    assert cfg.analysis.lookback_days == 90
    assert cfg.analysis.min_commits == 5
    assert cfg.analysis.top_n_owners == 3
    assert cfg.paths.exclude == ("*.tmp", "build/**")
    assert cfg.output.header == "# Custom header"
    assert cfg.output.include_unowned is True
    assert cfg.drift.mode == "repo"
    assert cfg.drift.compare_to == "main"
    assert cfg.notifications.webhook_url == "https://hooks.example.com/drift"
    assert cfg.notifications.include_unchanged is True


def test_load_config_invalid_yaml(tmp_path: Path) -> None:
    root = _write_config(tmp_path, "[[[invalid yaml")
    with pytest.raises(yaml.YAMLError):
        load_config(repo_root=root)


def test_load_config_non_mapping(tmp_path: Path) -> None:
    root = _write_config(tmp_path, "- a\n- b\n")
    with pytest.raises(ValueError, match="expected a YAML mapping"):
        load_config(repo_root=root)


def test_load_config_unknown_keys_ignored(tmp_path: Path) -> None:
    root = _write_config(tmp_path, "custom_field: true\nanother: 42\n")
    cfg = load_config(repo_root=root)
    assert cfg == Config()


def test_load_config_custom_repo_root(tmp_path: Path) -> None:
    sub = tmp_path / "myrepo"
    sub.mkdir()
    root = _write_config(sub, "analysis:\n  min_commits: 10\n")
    cfg = load_config(repo_root=root)
    assert cfg.analysis.min_commits == 10


def test_drift_mode_literal(tmp_path: Path) -> None:
    for mode in ("commit", "repo", "both"):
        root = _write_config(tmp_path, f"drift:\n  mode: {mode}\n")
        cfg = load_config(repo_root=root)
        assert cfg.drift.mode == mode
        # Clean up for next iteration
        (tmp_path / ".github" / "checkowners.yml").unlink()
        (tmp_path / ".github").rmdir()


def test_paths_exclude_override(tmp_path: Path) -> None:
    root = _write_config(tmp_path, "paths:\n  exclude:\n    - '*.tmp'\n")
    cfg = load_config(repo_root=root)
    assert cfg.paths.exclude == ("*.tmp",)
    assert "*.lock" not in cfg.paths.exclude
