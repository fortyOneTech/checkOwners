"""Tests for checkowners.generate module."""

from __future__ import annotations

from datetime import UTC, datetime
from pathlib import Path
from unittest.mock import patch

from checkowners.generate import (
    _build_codeowners_content,
    _collect_owned_paths,
    _collect_unowned_paths,
    _normalize_path,
    generate_codeowners,
)
from checkowners.models import (
    AnalysisConfig,
    Config,
    GithubConfig,
    OutputConfig,
    OwnerEntry,
    OwnershipMap,
    PathOwnership,
)

_NOW = datetime(2026, 5, 28, 12, 0, 0, tzinfo=UTC)


def _entry(handle: str, confidence: float, commits: int = 5) -> OwnerEntry:
    return OwnerEntry(
        handle=handle,
        confidence=confidence,
        last_commit=_NOW,
        commits=commits,
    )


def _make_ownership(
    raw: dict[str, tuple[OwnerEntry, ...]],
) -> OwnershipMap:
    paths = {p: PathOwnership(owners=owners, bus_factor=len(owners)) for p, owners in raw.items()}
    return OwnershipMap(paths=paths, last_analyzed=_NOW)


def _zero_threshold() -> Config:
    return Config(analysis=AnalysisConfig(confidence_threshold=0.0))


def test_generate_writes_file(tmp_path: Path) -> None:
    ownership = _make_ownership({"src/main.py": (_entry("alice@example.com", 0.9),)})
    config = _zero_threshold()
    content = generate_codeowners(tmp_path, ownership, config)
    codeowners = tmp_path / ".github" / "CODEOWNERS"
    assert codeowners.exists()
    assert codeowners.read_text(encoding="utf-8") == content


def test_generate_includes_header() -> None:
    ownership = _make_ownership({"src/main.py": (_entry("alice@example.com", 0.9),)})
    config = _zero_threshold()
    content = _build_codeowners_content(ownership, config)
    assert content.startswith("# Generated by checkOwners. Do not edit manually.\n")


def test_generate_custom_header() -> None:
    ownership = _make_ownership({"src/main.py": (_entry("alice@example.com", 0.9),)})
    config = Config(
        analysis=AnalysisConfig(confidence_threshold=0.0),
        output=OutputConfig(header="# Custom header"),
    )
    content = _build_codeowners_content(ownership, config)
    assert content.startswith("# Custom header\n")


def test_generate_paths_sorted() -> None:
    ownership = _make_ownership(
        {
            "z.py": (_entry("alice@example.com", 0.9),),
            "a.py": (_entry("bob@example.com", 0.9),),
            "m.py": (_entry("carol@example.com", 0.9),),
        }
    )
    content = _build_codeowners_content(ownership, _zero_threshold())
    lines = [line for line in content.splitlines() if line and not line.startswith("#")]
    assert lines[0].startswith("/a.py")
    assert lines[1].startswith("/m.py")
    assert lines[2].startswith("/z.py")


def test_generate_owners_confidence_ordered() -> None:
    ownership = _make_ownership(
        {"src/api.py": (_entry("alice@example.com", 0.95), _entry("bob@example.com", 0.6))}
    )
    content = _build_codeowners_content(ownership, _zero_threshold())
    assert "/src/api.py alice@example.com bob@example.com" in content


def test_generate_confidence_threshold_filters_owners() -> None:
    ownership = _make_ownership(
        {"src/main.py": (_entry("alice@example.com", 0.4), _entry("bob@example.com", 0.1))}
    )
    config = Config(analysis=AnalysisConfig(confidence_threshold=0.3))
    content = _build_codeowners_content(ownership, config)
    assert "alice@example.com" in content
    assert "bob@example.com" not in content


def test_generate_confidence_threshold_drops_path_when_all_owners_filtered() -> None:
    ownership = _make_ownership(
        {"src/main.py": (_entry("alice@example.com", 0.1),)}
    )
    config = Config(analysis=AnalysisConfig(confidence_threshold=0.3))
    content = _build_codeowners_content(ownership, config)
    assert "src/main.py" not in content


def test_generate_include_confidence_annotates() -> None:
    ownership = _make_ownership(
        {"src/main.py": (_entry("alice@example.com", 0.92), _entry("bob@example.com", 0.71))}
    )
    config = Config(
        analysis=AnalysisConfig(confidence_threshold=0.0),
        output=OutputConfig(include_confidence=True),
    )
    content = _build_codeowners_content(ownership, config)
    assert "/src/main.py alice@example.com bob@example.com  # alice@example.com(0.92) bob@example.com(0.71)" in content
    assert "Confidence scores reflect each owner's expertise" in content


