def test_generate_with_team_resolution() -> None:
    ownership = _make_ownership(
        {
            "src/main.py": (_entry("@alice", 0.9), _entry("@bob", 0.8)),
            "src/util.py": (_entry("@carol", 0.9),),
        }
    )
    config = Config(
        analysis=AnalysisConfig(confidence_threshold=0.0),
        github=GithubConfig(org="myorg", resolve_teams=True),
    )

    def mock_resolver(owners: tuple[str, ...]) -> str | None:
        if set(owners) == {"@alice", "@bob"}:
            return "@myorg/backend"
        return None

    with patch("checkowners.github.create_team_resolver", return_value=mock_resolver):
        content = _build_codeowners_content(ownership, config, token="ghp_test", org="myorg")
    assert "/src/main.py @myorg/backend" in content
    assert "/src/util.py @carol" in content


def test_generate_team_resolution_disabled() -> None:
    ownership = _make_ownership(
        {"src/main.py": (_entry("@alice", 0.9), _entry("@bob", 0.8))}
    )
    config = Config(
        analysis=AnalysisConfig(confidence_threshold=0.0),
        github=GithubConfig(resolve_teams=False),
    )
    content = _build_codeowners_content(ownership, config, token="ghp_test", org="myorg")
    assert "@alice @bob" in content


def test_generate_with_codeowners_path(tmp_path: Path) -> None:
    ownership = _make_ownership({"f.py": (_entry("alice@example.com", 0.9),)})
